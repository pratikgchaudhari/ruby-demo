print "Enter a integer: "
integer = gets.chomp.to_i
puts "#{integer} * 10 = #{integer * 10}"

print "\nEnter another integer: "
integer = gets.chomp.to_i
puts "#{integer} / 10 = #{integer/10}"

print "\nEnter a floating point number: "
float = gets.chomp.to_f
puts "#{float} * 10 = #{float * 10}"

print "\nEnter another floating point number: "
float = gets.chomp.to_f
puts "#{float} / 10 = #{float / 10}"